    #
    @Core.DEB_MEMBER_FUNCT
    def getImage(self,image_id) :
        self._data_cache = self.__control.ReadImage(image_id)
        dataflat = self._data_cache.buffer.ravel()
        dataflat.dtype = numpy.uint8
        return dataflat

